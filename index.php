 <div class="text-center"> <h2 class="text-danger" ><i class="fa fa-book"></i> ABOUT FEDERAL HIGH COURT MAKURDI</h2></div>
The Federal Revenue Court (as Federal High Court was then called) was established by the Federal Revenue Act 1973 (1973 No.13). The Court was renamed the Federal High Court by Section 228 (1) and 230 (2) of the Constitution of the Federal Republic of Nigeria, 1979. Although the need was noted during the constitutional Conference leading to Independence, to establish a High Court for the determination of causes and matters within the Exclusive Legislative list, as is the customary in countries with the Federal System of Government, no step was however taken in that regard until the promulgation of the Federal Revenue Court Decree in 1973.

